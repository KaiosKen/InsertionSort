import Foundation

var newArray = unsortedIntegers
var swapCount = 0
var totalSwapCount = 0
var passCount = 0

print("Pass: \(passCount), Swaps: \(swapCount)/\(totalSwapCount), Array: \(newArray)")
for x in 1..<newArray.count {
    var y = x
    while y > 0 && newArray[y] < newArray[y - 1] {
        newArray.swapAt(y-1,y)
        y -= 1
        swapCount += 1
    }
    passCount += 1
    totalSwapCount += swapCount
    print("Pass: \(passCount), Swaps: \(swapCount)/\(totalSwapCount), Array: \(newArray)")
    swapCount = 0
}
